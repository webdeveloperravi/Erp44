    public static function getStoreAccountIds($userId)
    {    
        $storeId = StoreHelper::getUserStoreId($userId);
        $ids =  UserStore::where('org_id',$storeId)->whereIn('type',['user','bank'])->pluck('id')->toArray();
        return array_merge($ids,[$storeId]); 
    }
    
    
    
